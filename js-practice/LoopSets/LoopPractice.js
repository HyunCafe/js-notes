// 3) Write a function that takes in a number and prints the even numbers from 1 to that number to the console. 
function countEven(num) {
    for (let i = 1; i < num; i++) {
        if (i % 2 === 0) {
            console.log(i);
        }
    }
}
countEven(15);

// 4) Write a function that takes in a number and prints the odd numbers from 1 to that number to the console.
function counterOdd(num) {
    for (i = 1; i < num; i++) {
        if (i % 2 !== 0) {
            console.log(i);
        }
    }
}
counterOdd(14)

